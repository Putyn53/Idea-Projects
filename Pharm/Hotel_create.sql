-- SELECT służący znalezieniu które pokoje znajdują się w których hotelach
SELECT IDPOKOJ, NR_POKOJU, HOTEL.IDHOTEL, HOTEL.NAZWA_HOTELU
FROM POKOJ LEFT JOIN HOTEL ON POKOJ.IDHOTEL = HOTEL.IDHOTEL;

-- SELECT liczący ilość pokoi w danych hotelach
SELECT HOTEL.NAZWA_HOTELU, COUNT(POKOJ.IDPOKOJ) AS ILOSC_POKOI
FROM POKOJ LEFT JOIN HOTEL ON POKOJ.IDHOTEL = HOTEL.IDHOTEL
GROUP BY HOTEL.NAZWA_HOTELU;

-- UPDATE zmieniający datę płatności na datę wymeldowania pod warunkiem, że wybrana została płatność kartą
UPDATE REZERWACJA
SET data_platnosci = data_wymeldowania
WHERE idplatnosc = 1;

-- DELETE usuwający błędnie wprowadzone dane, gdzie data płatności jest późniejsza od daty wymeldowania. Zakładamy, że nie można płacić po wymeldowaniu.
DELETE FROM REZERWACJA WHERE DATA_PLATNOSCI > DATA_WYMELDOWANIA;

-- SELECT pozwalający na ukazanie pokoi w hotelach, które kosztują ponad średnią
SELECT HOTEL.NAZWA_HOTELU HOTEL, POKOJ.NR_POKOJU POKOJ, POKOJ.CENA_DZIEN CENA FROM POKOJ
LEFT JOIN HOTEL ON POKOJ.IDHOTEL = HOTEL.IDHOTEL
WHERE POKOJ.CENA_DZIEN > (SELECT AVG(POKOJ.CENA_DZIEN) FROM POKOJ)
AND POKOJ.IDSTANDARD = 3;

-- SELECT pozwalający na sprawdzenie ilosci rezerwacji przypadających na pokoje
SELECT IDPOKOJ, (
    SELECT COUNT(IDREZERWACJA)
    FROM REZERWACJA
    WHERE POKOJ.IDPOKOJ = REZERWACJA.IDPOKOJ) "ILOSC REZERWACJI NA POKOJ"
FROM POKOJ
ORDER BY "ILOSC REZERWACJI NA POKOJ" DESC







